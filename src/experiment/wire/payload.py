# -*- coding: utf-8 -*-
"""
Module wire/payload.py
======================

Mock payload bytes for outbound client requests. Each request carries a real ASCII blob whose length matches the declared `size_bytes`; the byte count is what feeds the memory-usage dimensional coefficient downstream (no `psutil`, no process-memory probing).

Public API:
    - `MockPayload` frozen container for the generated bytes.
    - `generate_payload(kind, size_bytes, rng)` factory; deterministic under a seeded RNG.
    - `resolve_size_for_kind(sizes_by_kind, kind, default)` config-map lookup.
"""
# native python modules
from __future__ import annotations

import random
import string

# data types
from dataclasses import dataclass
from typing import Any, Dict, Optional


# ASCII-only alphabet so one character equals one UTF-8 byte
_ALPHABET = string.ascii_letters + string.digits


@dataclass(frozen=True)
class MockPayload:
    """*MockPayload* one generated dummy payload carried verbatim on the wire.

    Attributes:
        kind (str): request kind label this payload was generated for.
        size_bytes (int): declared payload size in bytes.
        blob (str): ASCII-only string of exactly `size_bytes` bytes.
    """

    # request kind label, e.g. "analyse_request", "alarm_request", "drug_request", "response_default"
    kind: str

    # declared payload size in bytes generated in UTF-8
    size_bytes: int

    # ASCII-only string of exactly `size_bytes` bytes
    blob: str

    def to_dict(self) -> Dict[str, Any]:
        """*to_dict()* serialise to a plain dict for JSON transport on `SvcReq.payload`.

        Returns:
            Dict[str, Any]: `{"kind", "size_bytes", "blob"}` mirroring the dataclass fields.
        """
        _ans = {
            "kind": self.kind,
            "size_bytes": self.size_bytes,
            "blob": self.blob,
        }
        return _ans


def generate_payload(kind: str,
                     size_bytes: int,
                     rng: Optional[random.Random] = None) -> MockPayload:
    """*generate_payload()* synthesise a `MockPayload` whose blob length is exactly `size_bytes`.

    Draws characters from an ASCII-only alphabet so the UTF-8 encoding length equals the character count, making the on-the-wire byte count exact.

    Args:
        kind (str): request kind label the payload belongs to.
        size_bytes (int): requested payload size in bytes; must be `>= 0`.
        rng (Optional[random.Random]): seeded RNG for deterministic reproduction. When `None`, a fresh `random.Random()` is used.

    Raises:
        ValueError: if `size_bytes < 0`.

    Returns:
        MockPayload: payload with a blob of exactly `size_bytes` ASCII bytes.
    """
    if size_bytes < 0:
        _msg = f"size_bytes must be >= 0, got {size_bytes}"
        raise ValueError(_msg)

    if rng is None:
        _rng = random.Random()
    else:
        _rng = rng

    # one char equals one UTF-8 byte under the ASCII alphabet
    _blob = "".join(_rng.choices(_ALPHABET, k=int(size_bytes)))
    return MockPayload(kind=kind, size_bytes=int(size_bytes), blob=_blob)


def resolve_size_for_kind(sizes_by_kind: Dict[str, int],
                          kind: str,
                          default: int = 256) -> int:
    """*resolve_size_for_kind()* read the declared payload size for `kind` from the method-config map.

    The method config keys requests with literal kind labels (`analyse_request`, `alarm_request`, ...) but the client uses kind labels equal to target artifact names (`TAS_{2}`, `TAS_{3}`, ...). This helper bridges both conventions and falls back to a `response_default` entry, then to the caller-supplied scalar.

    Args:
        sizes_by_kind (Dict[str, int]): method-config's `request_size_bytes` map.
        kind (str): kind label (either form).
        default (int): fallback when neither `kind`, the `<kind>_request` alias, nor `response_default` is present in the map.

    Returns:
        int: size in bytes to generate for this kind.
    """
    if kind in sizes_by_kind:
        return int(sizes_by_kind[kind])
    _alias = f"{kind}_request"
    if _alias in sizes_by_kind:
        return int(sizes_by_kind[_alias])
    return int(sizes_by_kind.get("response_default", default))
